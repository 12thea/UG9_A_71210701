#data harga makanan
IkanBakar = 25000
EsDoger = 6000
RujakCingur = 8000

#proses
print("=====MASUKAN JUMLAH YANG DIPESAN=====")
Makanan1 = int(input("ikan bakar  Rp 25.000,00  : "))
Makanan2 = int(input("es doger    Rp  6.000,00  : "))
Makanan3 = int(input("rujak cigur Rp  8.000'00  : "))

#total harga
m1 = IkanBakar*Makanan1
m2 = EsDoger*Makanan2
m3 = RujakCingur*Makanan3

print("=====TOTAL=====")
print("TOTAL IKAN BAKAR     :Rp ",m1)
print("TOTAL ES DOGER       :Rp ",m2)
print("TOTAL RUJAK CINGUR   :Rp ",m3)

#total
total = m1+m2+m3
print("Jumlah total biaya yang harus dibayar adalah Rp",total)

